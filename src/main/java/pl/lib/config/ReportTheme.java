package pl.lib.config;

/**
 * Enum defining available visual themes for reports.
 * Each theme provides a predefined set of styles and colors
 * to ensure consistent appearance across different reports.
 */
public enum ReportTheme {
    /**
     * Default professional theme with blue accents
     */
    DEFAULT,

    /**
     * Classic theme with black and white styling
     */
    CLASSIC,

    /**
     * Modern theme with green accents
     */
    MODERN,

    /**
     * Corporate theme with navy blue styling
     */
    CORPORATE,

    /**
     * Minimal theme with gray tones
     */
    MINIMAL
}
